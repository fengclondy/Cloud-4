package com.ssit.Entity;

public class LibInfoEntity {
	private String name;
	private int libs;
	private int devices;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLibs() {
		return libs;
	}
	public void setLibs(int libs) {
		this.libs = libs;
	}
	public int getDevices() {
		return devices;
	}
	public void setDevices(int devices) {
		this.devices = devices;
	}


}
